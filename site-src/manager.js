function saveFolderHistory(folder) {
  if (!folder || isExcludedPath(folder) || state.privateMode) return;
  state.folderHistory = state.folderHistory.filter(f => f !== folder);
  state.folderHistory.unshift(folder);
  if (state.folderHistory.length > 5) state.folderHistory.pop();
  try {
    localStorage.setItem(CONFIG.folderHistoryKey, JSON.stringify(state.folderHistory));
  } catch {}
  renderFolderChips();
}

