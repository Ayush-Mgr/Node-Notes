function isVaultPathSafe(path, { isAsset = false } = {}) {
  const prefix = CONFIG.vaultPrefix;
  return typeof path === "string" 
    && path.startsWith(prefix) 
    && (isAsset || path.endsWith(CONFIG.noteSuffix)) 
    && !path.includes("..") 
    && !path.includes("\\");
}

function noteStemFromPath(path) {
  return path.split("/").pop().replace(/\.md$/i, "");
}

function buildFilename(title) {
  if (title.trim()) return `${title.trim().replace(/\.md$/i, "")}.md`;
  const pad = (n, l = 2) => String(n).padStart(l, '0');
  const d = new Date();
  return `${d.getUTCFullYear()}-${pad(d.getUTCMonth()+1)}-${pad(d.getUTCDate())}T${pad(d.getUTCHours())}-${pad(d.getUTCMinutes())}-${pad(d.getUTCSeconds())}-${pad(d.getUTCMilliseconds(), 3)}Z.md`;
}

function buildMarkdown(title, body, fallbackTitle) {
  const safeTitle = (title || fallbackTitle).replace(/"/g, "\\\"");
  return [
    "---",
    `title: "${safeTitle}"`,
    `date: ${new Date().toISOString()}`,
    "tags: [vault, web]",
    "---",
    "",
    body,
  ].join("\n");
}

function parseEditableNote(path, markdown) {
  const fallback = noteStemFromPath(path);
  const match = markdown.match(/^---\n([\s\S]*?)\n---\n?/);
  if (!match) return { title: fallback, body: markdown };

  const titleMatch = match[1].match(/^title\s*:\s*(?:"((?:\\"|[^"])*)"|'([^']*)'|(.+))\s*$/m);
  const rawTitle = titleMatch ? (titleMatch[1] || titleMatch[2] || titleMatch[3] || "") : "";
  return {
    title: rawTitle.replace(/\\"/g, "\"").trim() || fallback,
    body: markdown.slice(match[0].length)
  };
}

