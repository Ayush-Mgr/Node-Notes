function filteredNotes() {
  const query = state.searchQuery.trim();
  if (!query) return state.notes;
  if (!fuseIndex) {
    const qLower = query.toLowerCase();
    return state.notes.filter((note) => note.path.toLowerCase().includes(qLower));
  }
  const results = fuseIndex.search(query);
  return results.map(result => ({ ...result.item, matches: result.matches }));
}

function renderNoteList() {
  if (state.listStatus === "loading") {
    elements.noteList.innerHTML = '<p class="list-state">Scanning vault notes…</p>';
    elements.noteCount.textContent = "Syncing";
    return;
  }

  if (state.listStatus === "error") {
    elements.noteList.innerHTML = `<p class="list-state">${escapeHtml(state.listError || "Unable to load notes from GitHub.")}</p>`;
    elements.noteCount.textContent = "Error";
    return;
  }

  const notes = filteredNotes();
  if (notes.length === 0) {
    const message = state.isAuthenticated
      ? (state.searchQuery ? `No matches for "${escapeHtml(state.searchQuery)}".` : "No notes found in vault. Your new notes will appear here.")
      : "Please sign in with GitHub to manage your vault notes.";
    elements.noteList.innerHTML = `<p class="list-state">${message}</p>`;
    elements.noteCount.textContent = "0 files";
    return;
  }

  elements.noteCount.textContent = `${notes.length} files`;
  elements.noteList.innerHTML = notes.map((note) => {
    const confirmDelete = state.confirmDeletePath === note.path;
    const deleteLabel = confirmDelete ? "Confirm Delete" : "Delete";
    const titleHtml = highlightMatch(note.displayPath, note.matches, 'displayPath');
    
    return `
      <article class="vault-card">
        <div class="vault-card__top">
          <div class="vault-card__content">
            <h3 class="vault-card__title">${titleHtml}</h3>
          </div>
        </div>
        <div class="vault-actions">
          <button class="action-btn" type="button" data-action="edit" data-path="${escapeHtml(note.path)}" ${state.isMutating || !state.isAuthenticated ? "disabled" : ""}>Edit</button>
          <button class="action-btn danger ${confirmDelete ? "active-danger" : ""}" type="button" data-action="delete" data-path="${escapeHtml(note.path)}" data-sha="${escapeHtml(note.sha || "")}" ${state.isMutating || !state.isAuthenticated ? "disabled" : ""}>${escapeHtml(deleteLabel)}</button>
        </div>
      </article>
    `;
  }).join("");
}

