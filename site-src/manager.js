const mathExtension = {
  name: 'math',
  level: 'inline',
  start(src) {
    const m = src.match(/\$/);
    return m ? m.index : -1;
  },
  tokenizer(src, tokens) {
    const blockRule = /^\$\$(?:[\s\S]*?)\$\$/;
    const inlineRule = /^\$([^$\n]+?)\$/;
    let match;
    if ((match = blockRule.exec(src))) {
      return {
        type: 'math',
        raw: match[0],
        text: match[1],
        displayMode: true
      };
    } else if ((match = inlineRule.exec(src))) {
      return {
        type: 'math',
        raw: match[0],
        text: match[1],
        displayMode: false
      };
    }
  },
  renderer(token) {
    if (token.displayMode) return `\\[${escapeHtml(token.text)}\\]`;
    return `\\(${escapeHtml(token.text)}\\)`;
  }
};
if (typeof marked !== "undefined") {
  try {
    marked.use({ extensions: [mathExtension] });
  } catch {}
}

